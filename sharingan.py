import turtle as t
t.color("black","red")
t.pensize(7)
t.speed(100)
t.bgcolor("white")
t.begin_fill()
for i in range(90):
    t.right(i)
    t.circle(125,i,7)
    t.forward(i)
    t.right(90)
t.penup()
t.color("black","red")
t.right(90)
t.forward(340)
t.left(90)
t.forward(46)
t.pendown()
t.circle(176)
t.end_fill()
t.left(90)
t.penup()
t.forward(140)
t.right(90)
t.color("red","red")
t.begin_fill()
t.circle(30)
t.end_fill()
t.hideturtle()
t.done()